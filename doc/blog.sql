-- Corrections apportées par rapport à l'original :
--  1. Sens des FK inversé -> remis dans le bon sens (enfant -> parent)
--  2. "type" TYPE -> "type" "type" (référence correcte à l'enum)
--  3. PK composite (id, type) sur médias -> PK(id) seul
--  4. FK articles -> articles_medias dupliquée -> supprimée
--  5. Colonnes article_id/articles_id sur catégories et médias laissées
--     NULLables car redondantes avec les tables de jonction (N-N)
-- =========================================================

DROP TABLE IF EXISTS "articles_medias" CASCADE;
DROP TABLE IF EXISTS "articles_categories" CASCADE;
DROP TABLE IF EXISTS "catégories" CASCADE;
DROP TABLE IF EXISTS "médias" CASCADE;
DROP TABLE IF EXISTS "commentaires" CASCADE;
DROP TABLE IF EXISTS "articles" CASCADE;
DROP TABLE IF EXISTS "users" CASCADE;
DROP TYPE IF EXISTS "type";

CREATE TYPE "type" AS ENUM (
    'image',
    'video',
    'gif',
    'musique'
);

CREATE TABLE IF NOT EXISTS "users" (
    "id" INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    "pseudo" VARCHAR(255),
    "mail" VARCHAR(255),
    "mdp" VARCHAR(255),
    PRIMARY KEY("id")
);

CREATE TABLE IF NOT EXISTS "articles" (
    "id" INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    "titre" VARCHAR(255),
    "contenu" TEXT,
    "date" TIMESTAMP,
    "statut" BOOLEAN,
    "update" TIMESTAMP,
    "user_id" INTEGER,
    PRIMARY KEY("id")
);

CREATE TABLE IF NOT EXISTS "commentaires" (
    "id" INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    "contenu" TEXT,
    "user_id" INTEGER,
    "article_id" INTEGER,
    "date" TIMESTAMP,
    PRIMARY KEY("id")
);

CREATE TABLE IF NOT EXISTS "médias" (
    "id" INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    "type" "type",
    "url" VARCHAR(255),
    "articles_id" INTEGER, -- redondant avec articles_medias, laissé NULL dans le seed
    PRIMARY KEY("id")
);

CREATE TABLE IF NOT EXISTS "catégories" (
    "id" INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    "nom" VARCHAR(255),
    "description" VARCHAR(255),
    "article_id" INTEGER, -- redondant avec articles_categories, laissé NULL dans le seed
    PRIMARY KEY("id")
);

CREATE TABLE IF NOT EXISTS "articles_categories" (
    "id" INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    "article_id" INTEGER,
    "categorie_id" INTEGER,
    PRIMARY KEY("id")
);

CREATE TABLE IF NOT EXISTS "articles_medias" (
    "id" INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    "media_id" INTEGER,
    "article_id" INTEGER,
    PRIMARY KEY("id")
);

-- FK dans le bon sens : la table enfant référence la table parent
