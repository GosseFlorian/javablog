    ADD FOREIGN KEY("categorie_id") REFERENCES "catégories"("id")
    ON UPDATE NO ACTION ON DELETE NO ACTION;

ALTER TABLE "articles_medias"
    ADD FOREIGN KEY("article_id") REFERENCES "articles"("id")
    ON UPDATE NO ACTION ON DELETE NO ACTION;

